# 小彭的乱码
def print_hello(num):
    for i in range(0, num):
        print('hello')
        if __name__ == '__main__':
            print('你看不见我')


def print_world(num):
    for i in range(0, num):
        print('world')


def print_byebye(num):
    for i in range(0, num):
        print('byebye')

print_hello(1)