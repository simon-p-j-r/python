# 小彭的乱码
info_list = [['a', 1], ['b', 1]]
info_tuple = (('x', 3), ('y', 4))
info_dir = {}
for num in info_list:
    key_word = num[0]
    value_word = num [1]
    info_dir[key_word] = value_word
for num in info_tuple:
    key_word = num[0]
    value_word = num [1]
    info_dir[key_word] = value_word
print(info_dir)